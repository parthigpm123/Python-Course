'''Magic Methods & Dunder Methods in Python Explained Clearly

In Python, magic methods and dunder methods are essentially the same thing—special methods that you can define to customize the behavior of objects.

✅ Dunder Methods = Double UNDERscore methods

They are called dunder because their names begin and end with double underscores, like:

__init__, __str__, __add__, __len__, __getitem__, __setattr__, __call__ ...


These methods allow you to define how objects behave in certain situations, such as when you print them, compare them, or perform arithmetic.'''