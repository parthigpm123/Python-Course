'''
Escape Sequence	Meaning
\n	New line (line break)
\t	Horizontal tab
\\	Backslash (\) itself
\'	Single quote (')
\"	Double quote (")
'''

print("hello \nworld") #new line or line break


print("hello\tworld") # horizontal tab (space between words)


print("backslash:","hello\\world  ")

print("hello's world")

print("hello\'s world")

print("\nHello world")